 import {
     sum,
     min,
     pow
 } from './math';

 function cube(x) {
     return x * x * x;
 }
 const PI = Math.PI + Math.SQRT2;

 export default 5;

 function calculate(a, b) {
     let total = sum(a, b);
     let sbst = min(a, b);
     return pow(sbst, total)
 }

 export {
     cube,
     PI,
     calculate,
 }